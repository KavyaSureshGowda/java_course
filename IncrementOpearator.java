class IncrementOpearator{
public static void main(String[] args){
System.out.println("preincrement opearation.....");
int e=20;
int f=++e;
System.out.println("e:"+e);
System.out.println("f:"+f);
System.out.println("postincrement opearation.....");
int g=60;
int h=g++;
System.out.println("g:"+g);
System.out.println("h:"+h);
}
}


