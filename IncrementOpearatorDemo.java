class IncrementOpearatorDemo{
public static void main(String[] args){
System.out.println("preincrement opearation.....");
int a=1;
int b=++a;
System.out.println("a:"+a);
System.out.println("b:"+b);
System.out.println("postincrement opearation.....");
int i=100;
int j=i++;
System.out.println("i:"+i);
System.out.println("j:"+j);
}
}


